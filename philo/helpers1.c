#include "philo.h"

long	get_time(void)
{
	struct timeval	time;

	if (gettimeofday(&time, NULL) == -1)
		return (-1);
	return (time.tv_sec * 1000 + time.tv_usec / 1000);
}

void	ft_usleep(int timee)
{
	long	start;

	start = get_time();
	while (get_time() - start < timee)
		usleep(150);
}
